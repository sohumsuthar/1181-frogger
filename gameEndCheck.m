        if topL(1, i) == 14
            frog = frog +1;
        end
    end
    if frog == 5
        homes = zeros(1, 5);
        level = level + 1;
        lives = lives + 1;
        speed = (-0.2 * log((level+1)/3) + 0.8) * multiplier;

